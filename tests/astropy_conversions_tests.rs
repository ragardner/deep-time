    #[test]
    fn galexsec() {
        let galexsec = Dt::from_ymd(2020, 1, 1, Scale::TAI, 0, 0, 0, 0)
            .target(Scale::UTC)
            .to_galexsec()
            .to_sec_f();
        // galexsec  : 1261871963.0
        assert_eq!(galexsec, 1261871963.0);
    }

    #[test]
